package day3;

public class DataTypeDemo {

	public static void main(String[] args) {
		//byte
		byte myByteNum = 10;
		System.out.println("Byte value:"+myByteNum);
		
		//short
		short myShortNum = 100;
		System.out.println("Short value:"+myShortNum);
		
		//integer
		int myIntNum = 30000;
		System.out.println("Integer value:"+myIntNum);
		
		//long 
		long myLongNum = 18600000;
		System.out.println("Long value:"+myLongNum);

		
		//float type
		float myFloatNum = 5.99f;
		System.out.println("Float value:"+myFloatNum);
		
		//double
		double myDoubleNum = 3.1416;
		System.out.println("Double value:"+myDoubleNum);
		
		//char Type
		char myLetter='A';
		System.out.println("Character value:"+myLetter);
		
		//boolean type
		boolean myBool = true;
		
		System.out.println("Boolean value:"+myBool);
		
	}

}
